public class loops{
    public static void main(String[] args) {
        int contador = 0;//aqui igual ao for a diferença, crio a variavel antes
        while (contador <= 10) {//aqui chamo o While, chamo a variavel, passo o valor que quero que conte
            System.out.println(contador);//printo chamando a variavel 
            contador++;//aqui é onde eu digo que quero que ele conte na sequencia e não que ele apenas me de o valor final
            
        }
    }
}